function date_format_fis($fecha)
{
	$dia=substr($fecha,0,strpos($fecha,"/"));
	if(strlen($dia) < 2)
		$dia = "0".$dia;
	$mes=substr($fecha,strpos($fecha,"/")+1,strrpos($fecha,"/") - (strpos($fecha,"/")+1));
	if(strlen($mes) < 2)
		$mes = "0".$mes;
	$ano=substr($fecha,strrpos($fecha,"/")+1);
	$fecha=$ano."-".$mes."-".$dia;
	return $fecha;
}
function ultimoDia($mes,$ano){ 
    $udia=28; 
