function validarCampos()
{
    window.onload = function ()
    {
        document.encuestaform.addEventListener('submit', validarFormulario);
    }

    function validarFormulario(evObject)
    {
        evObject.preventDefault();
        var pregunta1 = parseInt(document.getElementById('p1a').value)+parseInt(document.getElementById('p1b').value)+parseInt(document.getElementById('p1c').value);
        var pregunta2 = parseInt(document.getElementById('p2a').value)+parseInt(document.getElementById('p2b').value)+parseInt(document.getElementById('p2c').value);
        var pregunta3 = parseInt(document.getElementById('p3a').value)+parseInt(document.getElementById('p3b').value)+parseInt(document.getElementById('p3c').value);
        var pregunta4 = parseInt(document.getElementById('p4a').value)+parseInt(document.getElementById('p4b').value)+parseInt(document.getElementById('p4c').value);
        var pregunta5 = parseInt(document.getElementById('p5a').value)+parseInt(document.getElementById('p5b').value)+parseInt(document.getElementById('p5c').value);
        var pregunta6 = parseInt(document.getElementById('p6a').value)+parseInt(document.getElementById('p6b').value);
        var total = pregunta1+pregunta2+pregunta3+pregunta4+pregunta5+pregunta6;

        if (total < 6)
        {
            window.alert('Todas las preguntas deben ser contestadas');
            formulario.focus();
        }
        else
        {
            miPopup = window.open("encuestaEFR_guardar.php?p1a="+p1a.value+"&p1b="+p1b.value+"&p1c="+p1c.value+
                "&p2a="+p2a.value+"&p2b="+p2b.value+"&p2c="+p2c.value+
                "&p3a="+p3a.value+"&p3b="+p3b.value+"&p3c="+p3c.value+
                "&p4a="+p4a.value+"&p4b="+p4b.value+"&p4c="+p4c.value+
                "&p5a="+p5a.value+"&p5b="+p5b.value+"&p5c="+p5c.value+
                "&p6a="+p6a.value+"&p6b="+p6b.value
                ,"miwin","width=500,height=150");
            miPopup.focus();

            document.encuestaform.submit();
        }
    }
}