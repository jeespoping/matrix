function bisiesto($year)
{
	//si es multiplo de 4 y no es multiplo de 100 o es multiplo de 400*/
	return(($year % 4 == 0 and $year % 100 != 0) or $year % 400 == 0);
}

function ver($chain)
{
	if(strpos($chain,"-") === false)
	{
		return $chain;
	}
	else
		return substr($chain,0,strpos($chain,"-"));
}
function ver1($chain)
{
